float bytes2Float(uint8_t *bytes_array) //converts bytes to float data type
{
	union //uses a union that occupies the same memory space
	{
		float f;
		uint8_t b[4];
	} u;

	for(int i = 0; i < 4; i++)
	{
		u.b[i] = bytes_array[i];
	}

	return u.f;
}

uint16_t bytes2Int(uint8_t *bytes_array) //converts bytes to integer data type
{
	uint16_t newInt = bytes_array[1] + (bytes_array[0] << 8);
	return newInt;
}



